#include "../../includes/libft.h"

/**
 * @brief Return the size of the given string '\0' excluded
 * 
 * @param s string to measure
 * @return size_t len of the string
 */
size_t	ft_strlen(const char *s)
{
	size_t	size;

	size = 0;
	while (s[size])
		size++;
	return (size);
}
