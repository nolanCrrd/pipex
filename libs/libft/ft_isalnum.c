#include "../../includes/libft.h"

/**
 * @brief check if an int correspond to an alphabetic or numeric character
 * 
 * @param c char to check
 * @return int boolean
 */
int	ft_isalnum(int c)
{
	return (ft_isalpha(c) || ft_isdigit(c));
}
