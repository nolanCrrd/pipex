#include "../../includes/libft.h"

/**
 * @brief check if the given int correspond to an alphabetic char
 * 
 * @param c int to verify
 * @return int boolean
 */
int	ft_isalpha(int c)
{
	return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'));
}
